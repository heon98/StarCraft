package starCraft.game.gameComponent;

public class Protoss {
	private String name;
	private int cost;
	private int ability;
	private int unitId;

	public Protoss(String name, int cost, int ability, int unitId) {
		super();
		this.name = name;
		this.cost = cost;
		this.ability = ability;
		this.unitId = unitId;
	}
}
